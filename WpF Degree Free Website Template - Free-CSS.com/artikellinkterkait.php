<font style="color: blue;text-decoration: underline;">Evaluasi Memori pada Peserta Pelatihan Sempoa</font><br><br>
Sempoa merupakan metode yang telah dipakai oleh orang-orang Cina, Jepang, dan Korea untuk meningkatkan kemampuan matematika. Metode ini telah berkembang ke negara-negara lain, termasuk Amerika. Peningkatan pada kemampuan matematika disebabkan oleh fungsi terkoordinasi dari kedua belahan otak. Pembelajaran dan memori di bidang apa pun dapat dicapai dengan mengkoordinasikan dan menganalisis rangsangan-rangsangan sensoris yang berbeda. Selain itu, berdasarkan hasil penelitian, pelatihan sempoa juga dapat meningkatkan memori jangka pendek secara keseluruhan. 50 anak-anak berusia antara 5 dan 12 tahun dari 2 sekolah reguler dan 50  lembaga sempoa yang memiliki IQ rata-rata dievaluasi memori jangka pendeknya sebelum dan sesudah 1-2 tahun. Tes memori dilakukan dengan menggunakan skala memori Wechsler, pemeriksaan keadaan mental mini (mini mental state examination), dan Mann-Buitar visual memory screen for objects. Hasil penelitian menunjukkan bahwa peserta pelatihan sempoa setelah 1-2 tahun memiliki memori visual dan auditori yang lebih baik bila dibandingkan dengan anak-anak yang tidak mempelajari metode sempoa.<br><br>
