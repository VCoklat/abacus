                    <h2 class="blog_title"><a href="blog-single.html">Apakah Brain Gym itu?</a></h2>
                    <p>
Brain Gym adalah senam otak yang yang dirancang untuk membantu fungsi otak agar bekerja lebih optimal dan efektif. Gerakan-gerakan pada senam otak ini didasarkan pada gagasan bahwa latihan fisik yang sederhana dapat membantu aliran darah ke otak dan dapat membantu meningkatkan keefektifan proses belajar dengan memastikan otak tetap waspada. Selain itu, gerakan-gerakan tersebut juga didesain agar gerakan tangan kanan dan tangan kiri tidak sama, sehingga membutuhkan konsentrasi dan keseimbangan kedua belah otak untuk menampilkan gerakan tersebut dengan tepat.<br><br>
Pada awalnya Senam otak atau Brain Gym dimanfaatkan bagi anak-anak yang mengalami kesulitan-kesulitan seperti ADD (Attention Deficit Disorder), hiperaktif, kerusakan otak, sulit berkonsentrasi, dan depresi. Dengan melakukan gerakan-gerakan Brain Gym, kesulitan di atas dapat diatasi. Namun, dalam perkembanganya, Brain Gym juga digunakan untuk berbagai macam tujuan, seperti memudahkan kegiatan belajar, meningkatkan kewaspadaan dan reflex bagi orang yang lanjut usia, dan sebagainya. Brain Gym sudah popular di Amerika dan Eropa karena telah terbukti manfaatnya dan juga karena gerakan-gerakanya bervariasi dari yang mudah hingga sulit dilakukan.

</p>
