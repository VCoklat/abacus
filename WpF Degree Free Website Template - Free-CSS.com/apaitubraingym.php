                    <h2 class="blog_title"><a href="blog-single.html">Manfaat Brain Gym</a></h2>
                   
                    <p>
Brain gym merupakan salah satu teknik belajar yang paling baik karena banyak manfaat yang terdapat dalam brain gym itu sendiri. Manfaat melakukan brain gym antara lain:<br>
1.Menyeimbangkan otak kanan dan otak kiri
<br><br>
Gerakan tangan kanan dan tangan kiri pada umumnya didesain berbeda atau berlawanan, sehingga secara perlahan otak kanan dan otak kiri akan seimbang melalui latihan yang rutin. Hal ini dikarenakan gerakan tangan kiri dikoordinasikan oleh otak kanan, sedangkan gerakan otak kanan dikoordinasikan oleh otak kiri. Ketika kedua tangan melakukan gerakan yang berbeda dalam waktu yang bersamaan, kedua otak akan bekerja sama dan berkoordinasi hingga mencapai keseimbangan.
<br><br>
2.Meningkatkan fokus, konsentrasi, efisiensi, dan kreativitas
<br><br>
Saat seseorang melakukan gerakan brain gym, otak mereka akan terstimulasi dengan seimbang. Hal ini akan membantu mencapai keadaan mental yang rileks dan tenang, sehingga memudahkan untuk memasuki fase konsentrasi dan fokus. Ketika otak memasuki fase tersebut, otak akan bekerja dengan lebih optimal, efektif, efisien, dan kreatif. Dengan demikian, kinerja otak akan semakin baik dan dapat menyebabkan peningkatan prestasi belajar maupun kerja.
<br><br>
3.Meningkatnya kemampuan berbahasa dan daya ingat
<br><br>
Seseorang yang melakukan brain gym secara rutin akan memiliki peredaran darah ke otak yang lebih lancar. Hal ini tidak hanya membuat otak rileks, namun juga dapat menambah kemampuan otak seseorang dalam bekerja dan mengingat sesuatu. Kemampuan berbahasa otak juga akan meningkat dengan asupan nutrisi dan oksigen yang lebih lancar ke otak.
<br><br>

4.Meningkatkan kualitas hubungan antar manusia
<br><br>
Selain meningkatkan kemampuan otak seeorang, brain gym juga dapat membuat seseorang menjadi rileks secara fisik maupun mental. Hal ini berpengaruh pada hubungan antar manusia karena saat seseorang menghadapi masalah dengan keadaan yang tenang atau rileks, mereka dapat mengambil keputusan terbaik untuk penyelesaian masalah tersebut.
<br><br>

5.Mengurangi stres
<br><br>
Seperti yang telah dijelaskan, bahwa dengan melakukan brain gym, otak seseorang akan mejadi lebih rileks, sehingga orang tersebut dapat mengontrol emosinya dan menyelesaikan permasalahan-permasalahan yang dihadapinya dengan lebih baik. Hal ini akan mengurangi kemungkinan munculnya stres yang dapat berpengaruhi pada kesehatan dan produktivitas orang tersebut.
<br><br>
6.Menjadi lebih sehat
<br><br>
Seseorang yang belajar atau bekerja terus menerus pasti akan merasakan kebosanan dan mengalami kelelahan atau kejenuhan. Seringkali mereka tidak meluangkan waktu untuk refreshing atau menyegarkan kembali tubuh dan pikiran mereka. Sama halnya dengan otak, otak yang terus menerus bekerja tanpa henti akan membuat otak itu tidak bekerja secara efektif dan membuat seseorang menjadi stres. Saat seseorang stress akan mudah bagi orang tersebut untuk terkena penyakit karena sistem tubuh kita tidak bekerja dengan baik. Maka dengan efek relaksasi yang didapat dari melakukan gerakan brain gym, seseorang dapat terhindar dari stress juga terhindar dari penyakit.

</p>
