                    <h2 class="blog_title"><a href="blog-single.html">Brain Gym</a></h2>
                   
                    <p>
Brain gym (senam otak) merupakan sesi yang bertujuan menyeimbangkan otak kanan dan kiri anak, serta meningkatkan fokus dan konsentrasi anak. Dengan keadaan otak yang lebih rileks, fokus, dan konsentrasi, diharapkan anak dapat lebih siap dan mudah untuk menerima dan mempelajari suatu hal, dalam konteks ini adalah pembelajaran metode sempoa. <br><br>
Sesi brain gym diberikan di awal pertemuan sebagai ice breaking untuk mempererat hubungan antara pengajar dengan anak dan antara anak yang satu dengan yang lainnya. Sesi ini merupakan sesi yang menyenangkan bagi anak karena terlihat seperti sesi bermain. Dengan adanya sesi ini, anak dapat lebih bersemangat dan termotivasi untuk datang dan belajar. Anak akan belajar dengan senang dan tidak stres. Dengan kata lain, anak akan menjadi lebih sehat secara psikologis.<br><br>
Pada sesi ini, anak akan diajarkan gerakan-gerakan brain gym atau senam otak. Anak-anak akan diajak untuk menggerakan tangan, kepala, kaki, dan/atau anggota tubuh mereka yang lainnya melalui gerakan-gerakan sederhana. Gerakan-gerakan tubuh tersebut akan memperlancarkan aliran darah anak, sehingga anak menjadi lebih sehat secara fisik. <br><br>
Gerakan-gerakan brain gym juga akan melibatkan koordinasi bagian kanan dan kiri tubuh. Secara disadari ataupun tidak, anak dilatih untuk fokus dan konsentrasi agar dapat memperagakan gerakan-gerakan yang diajarkan dengan tepat dan lancar. Dengan latihan yang rutin, otak anak akan mencapai keseimbangan karena kedua belah otak terstimulasi dengan seimbang.<br><br>
Sebagai tugas di rumah, anak akan diminta untuk berlatih gerakan-gerakan yang telah diajarkan, dimana mayoritas gerakan dapat dilakukan di mana saja dan kapan saja. Dengan demikian, diharapkan anak dapat memiliki rutinitas untuk menggerakan anggota tubuh mereka dan melatih kedua belah otak mereka.
</p>
