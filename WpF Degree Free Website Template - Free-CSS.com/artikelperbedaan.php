                    <p>

Seringkali muncul pertanyaan “Apa sih perbedaan Abacus Brain Gym dari pelajaran sempoa konvensional“. Terdapat beberapa hal yang sangat jelas berbeda pada sistem pembelajaran Abacus Brain Gym (ABG) dengan sempoa konvensional, antara lain :<br>
<div style="margin-left: 30px;">
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;1.Sistem pembelajaran ABG menggunakan software Abacus Explorer dari USA, sehingga untuk pekerjaan rumah (PR) siswa, ABG tidak lagi menggunakan kertas atau buku (paper less). <br>
Tidak hanya itu, terdapat banyak keuntungan lain yang dapat diperoleh dari penggunaan software ini. Pada pembelajaran sempoa pada umumnya, latihan soal lebih banyak menggunakan buku atau kertas soal. Di ABG, dengan menggunakan software Abacus Explorer, latihan soal akan bervariasi karena tidak hanya membaca soal saja, melainkan juga mendengarkan soal (listening). Variasi membaca soal juga terbagi menjadi dua, yakni paper mode (seluruh angka dalam satu soal muncul bersamaan) dan flash mode (angka pada soal muncul satu per satu). Mode listening dan reading in flash mode ini mengadaptasi metode hi-speed listening dari Jepang yang terbukti memiliki dampak positif bagi perkembangan otak. Metode hi-speed listening melalui reading in flash mode dan listening melalui software ini tidak terdapat pada pembelajaran sempoa konvensional lainnya. <br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Dengan adanya software ini, anak-anak juga dapat melihat nilai mereka secara langsung saat mereka selesai mengerjakan. Setelah mereka selesai mengerjakan PR mereka, mereka dapat langsung mengumpulkan PR mereka dengan memilih “submit”. Kemudian data PR anak-anak Abacus Brain Gym dari seluruh dunia akan digabungkan dan diolah. Sistem ini akan terhubung dengan website “Hall of Fame” yang akan menampilkan peringkat 50 besar anak berdasarkan tingkat kerajinannya yang dihitung berdasarkan jumlah PR atau file yang dikerjakan dan nilai rata-rata dari file yang dikerjakan anak per hari, minggu, bulan, dan tahun. Secara tidak langsung, anak dapat berlomba secara virtual setiap saat dengan seluruh siswa Abacus Brain Gym di seluruh dunia. Anak-anak dapat terpacu untuk menyelesaikan file sebanyak dan sebaik mungkin untuk menduduki posisi 50 besar atau bahkan 3 besar. <br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2.Pembelajaran sempoa terdiri dari dua metode, yakni metode dua tangan dan satu tangan. ABG menggunakan metode pembelajaran satu tangan dengan sempoa 1-4 (1 buah manik-manik di bagian atas dan 4 buah manik-manik di bagian bawah). Metode sempoa ABG menggunakan kurikulum dari Grand Master Taiwan. Metode ini telah terbukti efektif dan paling banyak digunakan di seluruh dunia hingga saat ini. <br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;3.Bila di pembelajaran sempoa lainnya hanya berfokus pada pengajaran dan latihan sempoa di kelas, di ABG terdapat sesi brain gym atau senam otak di setiap awal pertemuan yang bertujuan sebagai ice breaking agar anak tidak jenuh dan dapat bersemangat dalam belajar, membantu menyeimbangkan otak kanan dan kiri anak agar keadaan mentalnya menjadi lebih rileks dan tenang, serta meningkatkan kesehatan anak baik secara fisik maupun mental melalui gerakan-gerakan tubuh dan tangan yang sederhana. Brain Gym telah terbukti efektif untuk perkembangan otak dan kesehatan.<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;4.Di ABG juga terdapat sesi character building atau pembangunan karakter melalui story telling di setiap akhir pertemuan. Anak-anak akan dibacakan sebuah cerita anak-anak yang memiliki pesan moral dan mudah dimengerti, kemudian mereka akan dipandu untuk menemukan pelajaran yang dapat mereka tarik dari cerita tersebut. Kemudian mereka akan diminta untuk menceritakan kembali cerita yang mereka dengar tersebut kepada orang tua di rumah dan mempraktekan pesan moral yang telah dipelajari dalam kehidupan sehari-hari. Hal ini bertujuan untuk mempererat hubungan antara anak dengan orang tua, serta mendidik anak agar menjadi anak yang baik dan berkarakter positif. Selain itu, sesi character building juga dapat memotivasi anak dan sebagai refreshing. <br>
</div>
Sebagai kesimpulan, Abacus Brain Gym akan menciptakan anak yang CERDAS melalui pembelajaran sempoa, BAIK melalui sesi pembangunan karakter, dan SEHAT secara fisik dan mental melalui sesi brain gym. Otak dan karakter anak distimulasi secara optimal melalui latihan metode Abacus Brain Gym secara rutin. 
