                    <h2 class="blog_title"><a href="blog-single.html">Speed Hearing</a></h2>
                   
                    <p>
Kunci untuk kemampuan belajar yang optimal adalah membuka potensi dari otak seseorang. Sebuah penelitian telah menunjukkan bahwa mode membaca dan mendengar menggunakan teknologi berbasis komputer dapat merangsang pengembangan sel otak dan meningkatkan 19 fungsi otak, serta meningkatkan IQ seseorang hingga 5% sampai 25%.<br>
<br>
Penelitian speed hearing ini dipelopori oleh militer AS pada tahun 1956. Kemudian penggunaan teknik ini dipelopori oleh militer Jepang sejak tahun 1970.<br>
<br>
Penelitian tersebut menemukan bahwa otak manusia dapat dilatih dengan teknik “overhelming the reseptor” (memberikan rangsangan dalam tingkat tinggi secara terus-menerus). Dalam istilah neurologi, teknik tersebut disebut dengan “plastisitas dependen". Contoh paling umum dari konsep tersebut adalah saat kita berkendara dengan kecepatan tinggi selama 60-90 menit, lalu kita akan merasa sulit menyetir dengan kecepatan lambat. Hal ini disebabkan oleh otak kita yang telah terbiasa mendapat informasi yang datang dengan cepat, Abacus Brain Gym menerapkan prinsip yang sama dengan melatih siswa untuk mendengarkan dan memahami interaksi suara yang cepat. Dengan demikian, mereka terbiasa untuk menghitung secara cepat.<br><br>
<font style="color: red">Manfaat pelatihan speed hearing</font>
<br><br>
Pelatihan tentang speed hearing dilaksanakan sekitar 20 tahun lalu di Jepang. Kira-kira 700 ribu mahasiswa dan pimpinan dari berbagai perusahaan besar ikut berpartisipasi dalam kegiatan penelitian tersebut. Mereka diperdengarkan sebuah pesan yang dibacakan secara cepat untuk melatih pendengaran dan pemahaman mereka. Latihan seperti ini menstimulasi otak bagian kanan dan juga mengurangi kerja dari otak kanan mereka, sehingga membuat para peserta penelitian ini bisa mengurangi beban pikiran dari pekerjaan mereka. Latihan ini juga bermanfaat untuk meningkatkan kreativitas dan memperbaiki perilaku mereka dalam bekerja atau belajar. University of Tokyo melakukan sebuah penelitian yang mengindikasikan latihan berpikir cepat dapat merangsang 19 macam fungsi otak. Ditambah lagi, hal ini juga dapat mengurangi atau bahkan menghindarkan seseorang dari kira kira 23 jenis penyakit jantung.<br><br>
<font style="color: red">Pelatihan otak yang berbeda dengan yang lainnya</font>
<br><br>
Dengan menggabungkan pelatihan metode mental (bayangan) dan Speed ​​Listening, kami telah menciptakan pelatihan otak yang berbeda dengan yang lainnya. <br><br>
Pemahaman dan perhitungan intensif diperlukan dalam setiap kegiatan manusia karena sebagian besar pengolahan informasi aktual tidak dimasukkan ke otak kanan untuk pemrosesan yang lebih cepat. Dengan pelatihan metode sempoa yang dilakukan secara rutin, siswa akan mendapatkan akses tak terbatas ke belahan kanan otak mereka. Hal ini seperti saat kita belajar mengendarai sepeda dahulu, secara naluriah kita bisa menyeimbangkan diri kita tanpa harus memikirkannya setiap saat.
<br><br>
<font style="color: red">Komunikasi dengan volume dan kecepatan yang lebih tinggi antara sel otak </font>
<br><br>
Seperti yang kita telah ketahui bersama bahwa latihan angkat beban secara teratur akan menghasilkan otot yang lebih padat untuk tubuh kita. Hal yang sama berlaku untuk otak kita. Latihan mendengarkan dengan kecepatan tinggi secara reguler akan menghasilkan jaringan sinapsis yang jauh lebih padat (jalur saraf dengan lebih banyak titik sambung antar sel), serta mampu menghasilkan volume yang lebih tinggi dan kecepatan komunikasi yang lebih tinggi antara sel-sel otak. <br><br>
<font style="color: red"> Menggabungkan semuanya dalam satu kesatuan</font>
<br><br>
Untuk mendukung dan mengoptimalisasi pelatihan ini, kami menggunakan sistem pelatihan berbasis komputer (software) yang dikembangkan khusus dan speech synthesizers untuk mengendalikan kecepatan (kata per menit) secara akurat, durasi dan tingkat kesulitan setiap latihan. Program pelatihan ini dirancang pada dasarnya berdasarkan riset biofeedback EEG "Brain Wave Optimization". Hasil dari riset tersebut menunjukkan bahwa program pelatihan ini merupakan program progresif yang dapat dicapai (achievable) yang akan bermanfaat bagi setiap dan seluruh siswa. Abacus Brain Gym menjamin bahwa setiap siswa akan menunjukkan peningkatan fokus dan konsentrasi, pemikiran yang lebih cepat dan lebih intuitif, memori yang lebih baik, dan tentu saja kemampuan matematika yang luar biasa atau superior.
