&emsp;Pembelajaran sempoa  memiliki tujuan utama untuk menyeimbangkan otak kanan dan kiri anak. Di sisi lain, Brain Gym juga bertujuan utama menyeimbangkan kedua otak anak tersebut. Brain Gym dan pembelajaran sempoa akan saling melengkapi dalam proses penyeimbangan otak anak. Pembelajaran sempoa akan menggunakan pendekatan kognitif, sedangkan Brain Gym akan menggunakan pendekatan motorik.<br>
&emsp;Selain itu, seringkali anak jenuh bila hanya bekerja dengan angka saja. Oleh karena itu, sesi Brain Gym bertujuan sebagai refreshing atau ice breaking untuk menyegarkan pikiran anak dan memotivasi anak untuk belajar. Sesi brain gym ini juga dapat membuat keadaan mental anak menjadi tenang, sehingga lebih siap dan mudah dalam menerima informasi dan belajar.
Mengingat kurangnya gerakan tubuh pada mayoritas anak pada jaman sekarang yang lebih senang bermain dengan gadget, maka sesi Brain Gym dapat menjadi waktu anak untuk melakukan gerakan-gerakan tubuh dan tangan yang sederhana agar tubuh tidak kaku dan peredaran darah menjadi lebih lancar. Dengan demikian, Brain  Gym dapat menyebabkan anak sehat baik secara fisik maupun mental.<br>
&emsp;Kemudian, apa tujuan Character Building dalam pembelajaran sempoa Abacus Brain Gym? Saat ini, di Indonesia dan bahkan di dunia, dapat kita temukan banyak anak cerdas, namun hanya sedikit anak baik dan berkarakter. Pembangunan karakter positif harusnya dimulai sejak dini, terutama di masa golden age   (sejak lahir hingga bangku Sekolah Dasar). Oleh karena itu, Abacus Brain Gym Indonesia memanfaatkan momen ini untuk turut menanamkan nilai-nilai positif pada anak.<br>
&emsp;Setelah kondisi otak anak seimbang dan keadaan mentalnya tenang melalui Brain Gym dan pembelajaran sempoa, anak akan lebih mudah menerima informasi. Saat itulah, sesi Character Building akan diberikan dengan menceritakan cerita anak-anak yang memiliki pesan moral, kemudian anak akan dipandu untuk menemukan pesan yang dapat mereka pelajari dari cerita.Sebagai tugas di rumah, mereka diminta menceritakan cerita yang telah mereka dengar kepada orang tua mereka di rumah dan menerapkan nilai-nilai yang telah mereka pelajari di kehidupan sehari-hari. Tugas menceritakan cerita kepada orang tua juga diharapkan dapat mempererat hubungan dan komunikasi anak dengan orang tua yang seringkali kurang di jaman sekarang.
&emsp;Dengan demikian, dengan belajar metode Abacus Brain Gym, anak akan menjadi CERDAS melalui pembelajaran sempoa, BAIK melalui sesi Character Building, dan SEHAT secara fisik dan mental melalui sesi Brain Gym.<br>
