                   <h2> FAQ (Frequently Ask Questions) </h2>
<font style="color: red">Q:Apa itu Abacus Brain Gym?</font>
A: Abacus Brain Gym (ABG) adalah sebuah program pelatihan otak yang bertujuan untuk mengembangkan potensi maksimal anak melalui 3 metode, yakni brain gym (senam otak), pembelajaran metode sempoa, dan character building (pembangunan karakter) melalui cerita bermoral.

<font style="color: red">Q: Berapa usia minimal anak untuk mengikuti program ABG?</font>
A: Tidak ada usia minimal untuk mengikuti program ABG. Akan tetapi, untuk mengikuti program ini, anak harus mengerti dan mampu menuliskan angka 0 hingga 9 dengan tepat, serta mampu membedakan angka mana yang lebih besar.

<font style="color: red">Q: Pada usia berapakah, seorang anak disarankan mengikuti program ABG?</font>
A: Usia paling efektif untuk mempelajari program ABG adalah rentang usia 4 – 12 tahun dimana anak berada pada masa Golden Age. Usia yang paling efektif untuk mulai mengikuti program ABG adalah usia 5 tahun (saat masuk TK-B) dimana anak sudah mampu menulis angka, namun belum diajarkan berhitung dengan menggunakan jari.

<font style="color: red">Q: Apa perbedaan ABG dengan kursus sempoa yang lainnya?  </font>
A: ABG adalah pelatihan sempoa tercanggih dengan menggunakan: 
-Metode Hi-Speed Listening dari Jepang
-Kurikulum dari Grandmaster Taiwan
-Software komputer dari USA

<font style="color: red">Q: Apakah keuntungan yang diperoleh dari belajar sempoa melalui ABG?  </font>
A: Pembelajaran sempoa melalui metode ABG memiliki banyak keuntungan, antara lain:
•Meningkatkan akurasi dan kecepatan kemampuan berhitung atau penalaran logis. 
•Membangun kekuatan memori yang lebih kuat.  
•Meningkatkan kemampuan berimajinasi.
•Meningkatkan tingkat dan kecepatan pemahaman. 
•Meningkatkan kekuatan visualisasi atau observasi.  
•Mempertajam kekuatan mendengarkan atau penilaian suara yang baik.  
•Mempromosikan ketahanan atau konsentrasi yang lebih lama.  
•Menikmati kesenangan dalam belajar dan mengurangi stres. 
•Membangun kapasitas belajar dan kepercayaan diri yang tinggi. 
•Memperlancar peredaran darah dan meningkatkan kesehatan secara keseluruhan melalui pergerakan jari yang internsif

<font style="color: red">Q: Apa kelebihan yang diperoleh dari belajar sempoa dengan menggunakan software ABG? </font>
A: Dengan menggunakan software, anak tidak hanya berlatih sempoa dengan membaca soal, namun juga dengan mendengarkan soal dalam bahasa Inggris. Dengan menggunakan software, kecepatan soal diberikan dan waktu menjawab soal dapat diatur, sehingga anak dapat menyesuaikan dan meningkatkan kemampuan dan kecepatan berhitung mereka. Selain itu, anak dapat secara langsung melihat nilainya. Software ini juga terhubung dengan Hall of Fame dimana dapat dilihat peringkat 50 besar siswa Abacus Brain Gym yang ada di seluruh dunia berdasarkan tingkat kerajinan mengerjakan PR. Hal ini akan menciptakan suasana seperti perlombaan, sehingga anak akan terus berlatih untuk mencapai nilai yang terbaik.
 
<font style="color: red">Q: Apa keistimewaan ABG? </font>
A: Selain metode pembelajaran sempoa yang memiliki 3 keunggulan yang akan memaksimalkan potensi otak anak menjadi anak CERDAS, ABG juga memiliki sesi brain gym (senam otak) di setiap awal pertemuan dan sesi character building (pembangunan karakter) di setiap akhir sesi. ABG juga menyelenggarakan lomba secara virtual melalui Hall of Fame yang dapat diikuti oleh seluruh siswa Abacus Brain Gym.

<font style="color: red">Q: Mengapa ABG mengkombinasikan pengajaran metode sempoa dengan senam otak dan pembangunan karakter? </font>
A: Tujuan utama ABG memberikan pengajaran sempoa adalah untuk memaksimalkan potensi otak anak dengan cara menyeimbangkan otak kanan dan kiri. Senam otak atau brain gym juga bertujuan untuk menyeimbangkan kedua belah otak. Selain itu, gerakan brain gym yang sederhana dan menyenangkan juga dapat dijadikan sebagai ice breaking agar anak termotivasi dalam belajar dan lebih konsentrasi. Dengan melakukan gerakan brain gym, anak akan menggerakan tangan, kaki, dan anggota tubuhnya, sehingga peredaran darah lebih lancar dan anak menjadi lebih sehat.

Setelah otak anak teroptimalisasi dan mudah untuk menyerap informasi, ABG menggunakan kesempatan ini untuk menanamkan nilai-nilai dan karakter positif pada anak melalui cerita anak-anak yang bermoral. Anak cerdas ada banyak, tetapi anak baik masih jarang. Melalui sesi pembangunan karakter ini, kemampuan anak memahami, menganalisis, dan menyimpulkan cerita dapat terlatih. Selain itu, ABG juga meminta anak untuk menceritakan cerita yang mereka dengar di kelas kepada orang tua di rumah, sehingga secara tidak langsung dapat mempererat hubungan dan komunikasi anak dengan orang tua.

Dengan demikian, dengan belajar di ABG, anak dapat menjadi CERDAS melalui pembelajaran metode sempoa, BAIK melalui sesi pembangunan karakter, dan SEHAT melalui sesi brain gym.

<font style="color: red">Q: Apakah Hall of Fame itu? </font>
A: Hall of Fame merupakan bagian website abacusbraingym.com yang menampilkan peringkat 50 besar siswa Abacus Brain Gym berdasarkan tingkat kerajinan siswa dalam mengerjakan PR dengan menggunakan software Abacus Brain Gym. Peringkat tersebut terdapat dalam 4 kategori, yakni harian, mingguan, bulanan, dan tahunan. Tingkat kerajinan tersebut diukur dari jumlah file yang diselesaikan siswa dalam periode waktu tertentu dan rata-rata nilai dari PR-nya. 

<font style="color: red">Q: Bagaimana cara anak bergabung dalam Hall of Fame? </font>
A: Anak yang merupakan siswa ABG hanya perlu mengerjakan PR / berlatih sempoa dengan menggunakan software, dengan catatan dalam 1 file anak harus menyelesaikan 10 soal (soal 1 – 10), kemudian mengumpulkan hasil latihan / PR-nya melalui menu ‘submit’ pada software. Waktu pengerjaan dan pengumpulan hasil latihan harus dilakukan sesuai dengan periode yang berlaku di kategori Hall of Fame.

<font style="color: red">Q: Apakah belajar sempoa akan mengganggu pelajaran matematika sekolah? </font>
A: TIDAK, karena metode sempoa merupakan LATIHAN OTAK yang terbukti efektif. Dengan belajar sempoa, anak akan mampu berhitung dengan cepat dan menguasai pelajaran matematika dengan lebih mudah. 
 
<font style="color: red">Q: Apakah anak akan bingung dengan pelajaran di sekolah apabila mempelajari metode sempoa?</font>
A: Selama ini TIDAK. Sebaliknya, anak-anak yang mempelajari metode sempoa mendapat nilai yang lebih baik dari anak yang tidak mempelajari sempoa. Hal ini dikarenakan sempoa dapat meningkatkan daya ingat, konsentrasi, dan imajinasi, serta membangun rasa percaya diri pada anak. Dengan kata lain, anak yang mempelajari metode sempoa akan lebih mudah untuk menyerap pelajaran-pelajaran lain. 
 
 <font style="color: red">Q: Bagaimana program ABG bisa membantu mengembangkan inteligensi anak?  </font>
A: Program ABG didesain tidak hanya untuk meningkatkan kemampuan berhitung dan pemahaman aritmatika anak, melainkan juga meningkatkan kekuatan ingatan, kreativitas, tingkat konsentrasi, kemampuan mendengarkan, serta kemampuan menganalisa anak. Dengan menggunakan metodologi yang terstruktur dan challenging-enhanced, ABG menstimulasi otak kanan dan kiri anak, sehingga dapat mencapai keseimbangan. ABG juga membantu anak-anak meningkatkan kemampuan berhitung dengan cepat, yakni 2 hingga 10 kali. Dengan menyeimbangkan otak kanan dan kiri anak selama proses pelatihan, para ahli mempercayai bahwa pelatihan sempoa (mental abacus) adalah kunci untuk mengoptimalkan kapasitas belajar anak.  
 
<font style="color: red">Q: Mengapa ABG menggunakan aritmatika sebagai subjek pelatihan, seberapa penting mental aritmatika?  </font>
A: Aritmatika sangat penting dalam membantu anak-anak membangun dasar akademik yang kuat. Guru yang mengajar anak kecil mengetahui bahwa tidak mudah untuk memperkenalkan konsep angka dan nilainya. Bagi anak kecil, memahami konsep matematika dalam bentuk abstrak merupakan proses yang panjang. Sulit bagi anak-anak untuk memahami hubungan numerikal tanpa menggunakan objek konkret, seperti biji sempoa untuk menghubungkan nilai numerik.  
 
Belajar bagaimana cara menghitung angka secara mental juga merupakan hal yang penting karena hal tersebut secara efektif mengasah bagian otak yang tidak terpakai sehingga meningkatkan inteligensi secara keseluruhan. Latihan mental aritmatika akan mempertajam kekuatan imajinasi dan memori. Berdasarkan penelitian ilmiah, seorang anak yang lemah dalam formasi mental akan mengalami kemunduran tajam dalam kemampuan mental bahkan sejak awal usia 20an. Di sisi lain, seorang anak yang memperoleh formasi ini di usia 12 tahun akan tetap cemerlang bahkan hingga usia 70 tahun.  
 
<font style="color: red">Q: Mengapa masih belajar sempoa saat kalkulator sudah tersedia? </font>
A: Walaupun kalkulator dan komputer telah menggantikan otak manusia untuk berhitung, belajar sempoa merupakan keharusan bagi setiap anak karena berperan dalam mengembangkan keseluruhan otak dan keterampilan hidup. Menggunakan sempoa membutuhkan koordinasi syaraf-syaraf utama dalam tubuh manusia, seperti penglihatan, suara, dan pergerakan jari, dimana hal tersebut akan mendorong pertumbuhan sel otak sehingga potensi otak akan termaksimalkan.  

 <font style="color: red">Q: Apa manfaat dari pengajaran speed hearing?  </font>
A: Sebuah penelitian di Universitas Tokyo menemukan bahwa pelatihan speed hearing dapat menstimulasi 19 fungsi otak. Sebagai tambahan, hal tersebut dapat membantu mencegah atau mengurangi resiko terhadap sekitar 23 jenis penyakit jantung.  
 
<font style="color: red">Q: Apakah program ABG tersedia di daerah lain? </font>
A: Untuk saat ini program ABG di Indonesia baru tersedia di daerah Serpong, tetapi sebelum masuk ke Indonesia, program ABG sudah terlebih dahulu masuk ke Jepang, Malaysia, Cina, Thailand, Singapura, Filipina, Australia, Oman, India, Kanada, UK, USA, etc. ABG merupakan satu-satunya lembaga yang menyediakan program sempoa dengan penekanan pada pendengaran dengan kecepatan tinggi dan penelitian gelombang otak yang ditingkatkan dengan teknologi komputer canggih. Ke depannya, program ABG akan hadir di daerah lain di Indonesia agar semakin banyak anak yang dapat menikmati pelatihan otak ini.

<font style="color: red">Q: Seperti apa program dan berapa lama waktu yang diperlukan untuk menyelesaikan program di ABG?  </font>
A: Program ABG dibagi menjadi 10 level dari pejumlahan dan pengurangan satu digit pada level yang paling mudah, kemudian berkembang hingga perkalian dan pembagian desimal dan dolar yang lebih kompleks. Setiap level terdiri dari 24 lesson. Jika anak rajin berlatih dan selalu menyelesaikan PR-nya, anak akan menyelesaikan 1 level dalam waktu 24 pertemuan.
