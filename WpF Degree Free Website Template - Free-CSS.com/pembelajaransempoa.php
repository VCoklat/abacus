Pembelajaran sempoa merupakan sesi utama pada kursus Abacus Brain Gym Indonesia yang bertujuan untuk mengoptimalkan potensi terbaik otak dengan menyeimbangkan otak kanan dan kiri anak.<br><br>
Pada sesi ini, anak akan berlatih berhitung dengan menggunakan sempoa dan mental (metode bayangan). Mental (metode bayangan) merupakan hasil yang diharapkan pada anak, dimana anak mampu berhitung dengan membayangkan pergerakan biji sempoa. Saat anak berhitung dengan menggunakan metode sempoa, otak kiri anak terstimulasi. Saat anak membayangkan biji-biji sempoa dan pergerakannya, otak kanan anak akan terstimulasi, sehingga kedua otak anak akan mencapai keseimbangan melalui latihan rutin.<br><br>
Pada sesi ini, anak akan mengerjakan soal-soal penjumlahan, pengurangan, perkalian, dan pembagian dengan tingkat kesulitan sesuai levelnya di kertas classwork. Anak akan diberikan  batas waktu untuk menyelesaikan soal yang diberikan kepada mereka. Anak akan dilatih untuk berpikir dan berhitung cepat, serta bekerja di bawah tekanan waktu.
Anak juga akan dilatih berhitung dengan cepat secara lisan (drilling) dengan menggunakan metode hi-speed listening. Latihan berhitung secara lisan tersebut dapat dilakukan dalam bahasa Indonesia dan Inggris.<br><br>
Sebagai tugas di rumah, anak akan mengerjakan PR (homework) dengan menggunakan software Abacus Explorer. Pada setiap pertemuan, anak diminta untuk menyelesaikan minimal 1 (satu) lesson pada software sesuai dengan lesson yang dikerjakan di kelas. Pada setiap lesson, anak harus menyelesaikan seluruh file yang terdiri dari mode membaca soal (reading) untuk soal penjumlahan, pengurangan, perkalian, dan pembagian, dan mode mendengarkan soal (listening) untuk soal penjumlahan dan pengurangan dalam bahasa Inggris. Secara tidak langsung, anak akan terlatih mendengarkan angka dalam bahasa Inggris. <br><br>Untuk mode listening dan reading in flash mode (mode membaca soal dimana angka muncul satu per satu), anak dapat mengatur kecepatan soal diberikan, jeda waktu antar angka, dan waktu mengetik jawaban, sehingga anak dapat berlatih di kecepatan yang nyaman bagi mereka dan dapat melatih diri untuk mengerjakan soal dengan lebih cepat.<br><br>
Setiap 1 (satu) file yang terdiri dari 10 (sepuluh) soal telah selesai dikerjakan, nilai anak akan secara langsung muncul di layar beserta dengan waktu yang dibutuhkan anak untuk menyelesaikan file tersebut. Anak juga dapat melihat nomor soal yang salah dan mengulang soal-soal yang salah tersebut untuk memeriksa kesalahan mereka. Apabila anak mendapat nilai kurang dari 70, anak wajib mengulang kembali file (10 soal) tersebut setelah mengerjakan 5 (lima) file yang lainnya untuk mencegah anak mengingat jawaban sebelumnya. File-file yang telah selesai dikerjakan dapat dikumpulkan melalui fitur ”submit”. Seluruh data PR anak akan diolah dalam Hall of Fame yang menampilkan 50 besar anak yang paling rajin mengerjakan PR, dihitung dari jumlah file yang diselesaikan dan rata-rata nilai anak, selama periode waktu tertentu. Hal ini akan memotivasi anak untuk berlomba-lomba mengerjakan file sebanyak-banyaknya dan sebaik mungkin.
