Setiap program reguler, terdiri dari 3 sesi, yakni sesi Brain Gym (senam otak), sesi Pembelajaran Sempoa, dan sesi Character Building (pembangunan karakter).
<br><br>
Pada setiap program, terdapat 10 level dimulai dari Grade 10 hingga Grade 1. 
<li>Grade 10 akan memperkenalkan konsep sempoa untuk operasi hitung tambah kurang yang akan meningkat jumlah digit dan angkanya pada level-level selanjutnya.</li>
<li>Grade 8 akan memperkenalkan konsep sempoa untuk operasi hitung perkalian dan pembagian yang akan meningkat jumlah digitnya pada level-level selanjutnya.</li>
<li>Grade 6 akan memperkenalkan konsep sempoa untuk operasi hitung tambah kurang untuk bilangan desimal yang akan meningkat jumlah digit dan angkanya pada level-level selanjutnya.</li>
<li>Grade 3 akan memperkenalkan konsep sempoa untuk operasi hitung perkalian dan pembagian untuk bilangan desimal dan dollar yang akan meningkat jumlah digitnya pada level-level selanjutnya.</li>
<br>
Metode mental / bayangan, yakni metode menghitung dengan membayangkan pergerakan biji sempoa tanpa menggunakan sempoa secara fisik, akan mulai diajarkan pada pertengahan Grade 10 dan akan meningkat jumlah digit dan angkanya secara berkala.
<br><br>
Setiap level terdiri dari 24 lesson, dimana pada lesson ke-24 akan dilakukan ujian kenaikan level yang terdiri dari 2 tipe ujian, yakni membaca soal di kertas dan mendengarkan soal melalui software Abacus Explorer.
